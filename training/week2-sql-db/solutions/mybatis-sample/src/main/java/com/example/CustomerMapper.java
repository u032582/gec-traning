package com.example;

/**
 * マッパー（Mapper）= 「Javaのメソッド」と「SQL」を結びつける窓口。
 *
 * これは interface（インターフェース）。メソッドの「宣言」だけを書き、中身（処理）は書かない。
 * 実際の処理は MyBatis が CustomerMapper.xml のSQLから自動で用意する。
 *
 * 結びつきのルール:
 *   このインターフェースの「メソッド名」 == CustomerMapper.xml の <select id="..."> の id
 *   → 名前が一致することで、findById() を呼ぶと id="findById" のSQLが実行される。
 *
 * ─────────────────────────────────────────────────────────────
 * 【演習でのあなたの作業（穴埋め）】
 *   下の findById メソッドの「1行の宣言」を自分で書くのが課題11の作業。
 *   ・戻り値の型: Customer（1件の顧客を返すため）
 *   ・メソッド名: findById（XMLの <select id="findById"> と一致させる）
 *   ・引数      : int id（取得したい顧客のid）
 *   ・末尾はセミコロン ; （インターフェースなので中身 {} は書かない）
 *
 *   このファイル（solutions配下）は「完成版（=答え）」なので、すでに書いてある。
 *   自分で書いた版と、この行を見比べて確認すること。
 * ─────────────────────────────────────────────────────────────
 */
public interface CustomerMapper {

    /**
     * 指定した id の顧客を1件取得する。該当が無ければ null を返す。
     */
    Customer findById(int id);   // ★ これが穴埋めの答え（この1行を自力で宣言できればOK）
}
